import UIKit

// MARK: - Extension

extension UIFont {
    // MARK: - Methods
    
    static func getCharter17() -> UIFont? {
        return UIFont.init(name: "Charter", size: 17)
    }
    
    static func getCharter55() -> UIFont? {
        return UIFont.init(name: "Charter", size: 55)
    }
    
    static func getAvenirNextRegular19() -> UIFont? {
        return UIFont.init(name: "Avenir Next Regular", size: 20)
    }
}
